package com.cbt.supersivor.po;

public class SupersivorInfo 
{
	private int id;
	private String supersivorNum;
	private String supersivorPwd;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSupersivorNum() {
		return supersivorNum;
	}
	public void setSupersivorNum(String supersivorNum) {
		this.supersivorNum = supersivorNum;
	}
	public String getSupersivorPwd() {
		return supersivorPwd;
	}
	public void setSupersivorPwd(String supersivorPwd) {
		this.supersivorPwd = supersivorPwd;
	}
	
}
